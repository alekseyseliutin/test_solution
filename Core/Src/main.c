/**
	* Обработка буфера данных от ADC. Вызывается из прерывания DMA раз 25мс.
  * Усредняет 125 значений из ADC и выставляет в DAC
	*/
void ADC_DMA_TransferComplete_Callback()
{
	uint32_t summ = 0;
	static uint32_t big_summ = 0;
	static uint8_t count = 0;
	for (uint16_t i=0; i<sizeof(ADC_data)/sizeof(ADC_data[0]) ;i++)
		summ+=ADC_data[i];
	big_summ+=summ;
	summ/=sizeof(ADC_data)/sizeof(ADC_data[0]);
  HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, summ);

	if (++count>=40)
	{
		HAL_GPIO_TogglePin(OUT_TICK_GPIO_Port, OUT_TICK_Pin);
		f_ready = 1;
		result = big_summ;
		big_summ = 0;
		count = 0;
	}

  LL_DMA_SetDataLength(DMA2, LL_DMA_STREAM_0, sizeof(ADC_data)/sizeof(ADC_data[0]));
  LL_DMA_EnableIT_TC(DMA2, LL_DMA_STREAM_0);
  LL_DMA_EnableStream(DMA2, LL_DMA_STREAM_0);
}
