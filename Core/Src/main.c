  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		if (f_ready==1)
		{
			f_ready = 0;
			
			result /= 5000;
			result *= 3300;
			result /= 0x0FFF;
#ifdef PRINTF_ENABLE
			printf("%d mV\r\n", result);
#endif
		}

  }
